<script>
// import _ from 'lodash'
export default {
  data () {
    return {
      isUnresolved: false,
      isResolved: false,
      isSubmitted: false,
      textareaValue: '',
      placeholderText: '',
      placeholderHack: false
    }
  },
  methods: {
    hasSubmit () {
      if (this.isSubmitted) {
        this.showToast({msg: '您已反馈'})
        return 1
      }
      if (this.isResolved || this.isUnresolved) {
        return 2
      }
    },
    sendFeddback (unresolveData) {
      let data = {
        problemDesc: '售后服务管理规则',
        problemFrom: this.$store.state.config.Plat,
        contactMobile: this.$store.state.userInfo.mobile,
        isSolve: 1
      }
      if (unresolveData) {
        // _.merge(data, unresolveData)
        data = {...data, ...unresolveData}
      }
      this.$post({
        url: this.$store.state.api.AddCommonProblem,
        data
      }).then(({data: res}) => {
        if (res.status == 1) {
          this.isSubmitted = true
          this.showToast({msg: '感谢反馈'})
        } else {
          this.showToast({msg: res.statusDetail})
        }
      })
    },
    onProblemResolved () {
      if (this.hasSubmit()) {
        return
      }
      this.isResolved = true
      this.sendFeddback()
    },
    onUnresolveBtnClick () {
      if (this.hasSubmit()) {
        return
      }
      this.isUnresolved = true
      // setTimeout(() => {
      //   this.$refs.problemProposal.focus()
      // }, 100)
    },
    onProposalSubmit () {
      if (this.hasSubmit() == 1) {
        return
      }
      let problemProposal = this.textareaValue.trim()
      if (problemProposal !== this.placeholderText && problemProposal) {
        this.sendFeddback({
          isSolve: 0,
          problemProposal
        })
      } else {
        this.showToast({msg: '请输入内容后再进行提交！'})
      }
    },
    onTextareaFocus (e) {
      if (!this.placeholderText) {
        return false
      }
      if (this.textareaValue === this.placeholderText) {
        this.textareaValue = ''
        this.placeholderHack = false
      }
    },
    onTextareaBlur () {
      if (!this.placeholderText) {
        return false
      }
      if (this.textareaValue === '') {
        this.textareaValue = this.placeholderText
        this.placeholderHack = true
      }
    }
  },
  created () {
    this.showLoad(false)
  },
  mounted () {
    // 低版本Android的placeholder不换行问题
    // let testInput = document.createElement("input")
    // let support = 'placeholder' in testInput && this.$store.state.config.Plat=='IOS'
    let support = false
    if (!support) {
      let textarea = this.$refs.problemProposal
      this.placeholderText = textarea.placeholder
      this.textareaValue = textarea.placeholder
      textarea.placeholder = ''
      this.placeholderHack = true
    }

    // 解决安卓虚拟键盘遮盖输入框问题
    if (/Android/gi.test(navigator.userAgent) || this.$store.state.config.Plat=='android') {
